// Build the combined dictionary
function buildYoDictionary(): Map<string, string> {
  const dict = new Map<string, string>();
  const sources = [PRONOUNS, ADVERBS, NOUNS, VERBS, ADJECTIVES, NUMERALS];

  for (const source of sources) {
    for (const [key, value] of Object.entries(source)) {
      // Only add entries where the key doesn't already contain ё
      // (those ARE the е→ё mappings)
      if (key !== value && !key.includes('ё')) {
        dict.set(key, value);
      }
    }
  }

  return dict;
}

